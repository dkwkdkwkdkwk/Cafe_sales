# ✅ 정규식으로 파싱: 자치구 / 카페명 / 메뉴 / 가격
pattern = re.compile(
    r"(서울\s)?(?P<district>[가-힣]+구)[/\s]+(?P<cafe>[^/\n]+)[/\s]+(?P<menu>[^/\n:]+)[/:]?\s*(?P<price>\d{3,5})원?"
)

# ✅ JSONL로 저장
with open("menu_items.json", "w", encoding="utf-8") as f:
    for match in pattern.finditer(raw_data):
        district = match.group("district").strip()
        menu = match.group("menu").strip()
        price = int(match.group("price").strip())
        # 카페명은 제외
        item = {"자치구": district, "메뉴": menu, "가격": price}
        f.write(json.dumps(item, ensure_ascii=False) + "\n")

